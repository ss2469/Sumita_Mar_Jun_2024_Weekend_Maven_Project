package com.java.Day8_7thApril;

public class Topic1_Arrays_introduction {

	public static void main(String[] args) {
		
		/* Array
		 * 1.Arrays are part of Data Structures
		 * 2. Arrays will store homogenous elements [data of same type]
		 * 3. Length of the array can be determined using length attribute
		 * 4. Size of the array is pre-determined
		 * 5. Indexing position of an array starts from 0
		 * 6. Arrays are represented in the form of dimension []
		 * 7. Parent class of array is Object class in java
		 */
		
		int a[] = {10, 20, 30, 40};
		boolean b[] = {true, false};
		
		
		// int[] c = new int[10];
		
		System.out.println("The length of array a is : " + a.length);
		System.out.println("The length of array b is : " + b.length);

	}

}
