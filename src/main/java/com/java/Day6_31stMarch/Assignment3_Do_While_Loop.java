package com.java.Day6_31stMarch;

public class Assignment3_Do_While_Loop {

	public static void main(String[] args) {
		// TODO Do While Loop example

		doWhileLoopExample();

	}

	public static void doWhileLoopExample() {

		int i = 10;

		do {
			System.out.println(i);
			i--;
		} while (i >= 0);

	}

}
